import turtle

turtle.bgcolor("black")
turtle.pencolor("red")  # setting the pencolor to red
turtle.backward(200)
turtle.left(120)  # setting the pen left at an angle of 90 degrees
turtle.backward(200)  # moving the pen backward at a unit of 100
turtle.left(120)  # setting the pen left at an angle of 90 degrees
turtle.backward(200)  # moving the code backward at 100 units

turtle.done()